  마. 개인정보사업과 관련한 고객 요구 및 이의, 불만에 대한 대응 계획<br>
   ○ 개인개인정보주체가 본인의 개인정보 이용·제공 사실의 열람을 요구할 경우 
      이에 대응할 수 있는 규정<br>
     ① 개인개인정보주체는 개인개인정보 수집 범위 및 이용약관의 내용 중 일부
        또는 개인개인정보의 이용·제공 목적, 제공받는 자의 범위 및 개인기반
        서비스의 일부에 대하여 동의를 유보할 수 있다.<br>
     ② 개인개인정보주체는 개인개인정보의 수집·이용·제공에 대한 동의의 전부
        또는 일부를 철회할 수 있다.<br>
     ③ 개인개인정보주체는 언제든지 개인개인정보의 수집·이용·제공의 일시적인 중지를
        요구할 수 있다. 이 경우 회사는 요구를 거절하지 아니하며, 이를 위한 
        기술적 수단을 갖추고 있음<br>
     ④ 개인개인정보주체는 회사에 대하여 아래 자료의 열람 또는 고지를 요구할 수
        있고, 당해 자료에 오류가 있는 경우에는 그 정정을 요구할 수 있다.
        이 경우 회사는 정당한 이유 없이 요구를 거절하지 아니합니다.<br>
         1. 개인개인정보주체에 대한 개인정보 수집·이용·제공사실 확인자료<br>
         2. 개인개인정보주체의 개인개인정보가 개인정보의 보호 및 이용 등에 관한 법률
             또는 다른 법령의 규정에 의하여 제3자에게 제공된 이유 및 내용<br>
     ⑤ 회사는 개인개인정보주체가 동의의 전부 또는 일부를 철회한 경우에는 
        지체 없이 수집된 개인개인정보 및 개인정보 수집ㆍ이용ㆍ제공사실 확인 자료를 파기한다.
        단, 동의의 일부를 철회하는 경우에는 철회하는 부분의 개인개인정보 및
        개인정보 수집·이용·제공사실 확인 자료에 한함<br>
     ⑥ 개인개인정보주체는 제1항 내지 제4항의 권리행사를 위하여 개인정보보호조직
        구성원의 연락처를 이용하여 회사에 정정 요청할 수 있다.<br>
  바. 개인정보의 수지사실 확인자료에 대한 열람·정정요구 계획<br>
   ○ 개인정보 수집 이용 제공 사실 확인자료의 열람 및 고지는 다음과 같은 절차에 따라 즉각적인 응답이 가능한 형태로 제공한다.<br>
     ① 개인개인정보주체의 열람 및 고지를 요구<br>
     ② 개인개인정보주체의 인적사항 확인 및 인증 또는 정당한 대리인(부모 등)임을 확인	- 시설관리자는 사전에 개인개인정보의 대한 수집 및 이용에 대한 동의서를 작성하고 이를 보관<br>
     ① 열람의 허용여부를 즉시 알리며, 열람 및 고지를 허용하지 않는 경우에는 그 사유가 근거를 설명하고 서면으로 기록 및 제공한다.
     ① 개인정보사업자 및 시설관리자는 열람 및 고지를 실시함에 있어서 제3자의
        개인개인정보 및 여타 개인정보가 누설되지 않도록 지정된 PC또는 계정으로
        관리자의 감독하에 열람을 하도록 한다.<br>
   ○ 다음의 경우는 개인개인정보주체의 열람고고지 및 정정요구를 거절 할 수 있다.<br>
	- 개인정보사업자등이 열람 또는 정정요구를 거절할 수 있는“정당한 이 유”로는 (ⅰ) 열람∙정정요구를 한 개인개인정보주체가 본인이 정당한 권한 있는 자임을 입증할 수 있는 본인확인을 위한 자료를 제출하지 않은 경우, (ⅱ) 지나치게 과도하고 반복적으로 열람요구를 하여 업무에 중 대한 지장을 초래하는 경우, (ⅲ) 「통신비밀보호법」등 법률에서 제한하고 있는 경우, (ⅳ) 본인이 아닌 제3자의 자료를 요구하는 경우 등이 해당 된다.<br>
   ○ 개인개인정보주체의 열람 및 고지 확인을 위해 아래와 같은 취급대장을 운영·관리한다.<br>
