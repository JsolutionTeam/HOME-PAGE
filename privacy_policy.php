라. 권한관리 내역의 기록 및 보관계획<br>
    ○ 접근권한 담당자는 개인정보 제공 서비스에 대해 반기 1회 접근권한을 검토하여야 하며
       상세관리방안은 다음과 같다.<br>
     - 개인정보관리자는 개인정보 제공 서비스에 대한 접근권한을 업무수행에 필요한 최소한의
       범위로 정하여 업무담당자에 따라 차등 부여하여야 한다.<br>
     - 개인정보관리자는 전보 또는 퇴직 등 인사이동이 발생하여 개인정보 취급자가 변경되었을
       경우 지체 없이 개인정보 제공 서비스의 접근권한을 변경 또는 말소하여야 한다.<br>
     - 개인정보관리자는 개인정보 제공 서비스에 대한 접근권한의 부여, 변경 또는 말소에
       대한 내역을 기록하고, 그 기록을 최소 3년간 보관하여야 한다.<br>
     - 개인정보관리자는 개인정보 제공 서비스에 접속할 수 있는 사용자 계정을 발급하는
       경우, 개인정보취급자 별로 사용자계정을 발급하여야 하며, 다른 개인정보취급자와
       공유되지 않도록 하여야 한다.<br>
     - 개인정보관리자는 개인정보취급자 또는 정보주체가 안전한 비밀번호를 설정하여
        이행할 수 있도록 비밀번호 작성 규칙을 수립하여 적용하여야 한다.<br>
<br>
