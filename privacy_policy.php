1. 보호계획의 수립 및 관리<br>
  가. 개인정보보호 정책 및 실현 계획<br>
   ○ 개인정보보호담당자는 개인정보의 안전한 처리를 위하여 개인정보를 관련한 법령 및 관련 규정을 준수하도록 다음 사항을 포함하여 내부 관리계획을 수립하여야 한다.<br>
    - 개인정보 보호책임자의 지정에 관한 사항<br>
    - 개인정보 보호책임자 및 개인정보관리자의 역할 및 책임에 관한 사항<br>
    - 개인정보의 안전성 확보에 필요한 조치에 관한 사항<br>
    - 개인정보취급자에 대한 교육에 관한 사항<br>
    - 그 밖에 개인정보 보호를 위하여 필요한 사항<br>
<br>
   ○ 개인정보 보호 담당자는 매년 1회 이상 내부 관리계획의 타당성과 개정 필요성을<br>
      검토하고 개정이 필요하다고 판단되는 경우 내부 관리계획 개정안을 작성하여<br>
      개인정보 보호책임자에게 보고하고 승인을 받아야한다. 타당성 검토에 고려되어야<br>
      하는 사항은 아래와 같다.<br>
    - 개인정보의 보호 및 이용 등에 관한 법률<br>
    - 적용기술의 변화<br>
    - 내/외부 이행점검 및 감사 결과 등<br>
<br>
  나. 내부관리계획의 공표<br>
   ○ 개인정보보호책임자는 개인정보 내부 관리계획을 수립하거나 개정안을 승인한 후 전 직원에게 공표하여야 한다.<br>
<br>
   ○ 내부 관리계획은 내부직원(비정규직 포함), 외부직원이 언제든지 열람할 수 있는 방법으로 비치하여야 하며, 변경사항이 있을 경우 이를 공지하여야 한다.<br>
    - 비치장소 : R&D 개발부<br>
    - 공지방법 : (내부) 전체공지, (외부) 서비스 페이지<br>
<br>
  다. 개인정보 취급대장의 운영, 관리 내용 및 방법<br>
   ○ 개인정보 취급 및 관리대장은 아래와 같은 양식으로 작성하며 아래 양식과 같이 전자적인 형태로 운영되어야 한다.<br>
   ○ 개인개인정보주체의 요구 및 이의·불만에 대응하기 위해 아래와 같은 취급대장을 운영·관리한다.<br>
